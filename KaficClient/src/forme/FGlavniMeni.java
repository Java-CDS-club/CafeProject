    public void showDate() {
        Date datum = new Date();
        SimpleDateFormat sf1 = new SimpleDateFormat("dd.MM.yyyy.");
        String datumString = sf1.format(datum);
        jlabDate.setText(datumString);

        SimpleDateFormat sf = new SimpleDateFormat("HH:mm:ss");
        String timeString = sf.format(datum);
        jlabTime.setText(timeString);
    }
